# 資料：程式的基本單位
# 數字
1234
0.5
# 字串
"測試中文"
"Hello World"
# 布林值
True
False
# 有順序、可動的列表 List
[3, 4, 5]
["Hello", "World"]
# 有順序、不可動的列表 Tuple
(3, 4, 5)
("Hellow", "World")
# 集合(沒有順序) Set
{3, 4, 5}
{"Hello", "World"}
# 字典 Dictionary (Key-Value)
{"apple": "蘋果", "date": "資料"}

# 變數：用來儲存資料的自訂名稱
# 變數名稱 = 資料
x = 3
# print(資料)
print(x)
x = True  # 取代舊的資料
print(x)
x = "Hello"
print(x)
x = {3, 5, 6}  # 集合 Set
print(x)
